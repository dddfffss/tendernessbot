const app = express();

// –£–∫–∞–∂–∏—Ç–µ —Ç–æ–∫–µ–Ω –≤–∞—à–µ–≥–æ Telegram-–±–æ—Ç–∞
const botToken = "–í–ê–®_–¢–û–ö–ï–ù_–ë–û–¢–ê"; // –ó–∞–º–µ–Ω–∏—Ç–µ –Ω–∞ –≤–∞—à —Ç–æ–∫–µ–Ω
const chatId = "–í–ê–®_CHAT_ID"; // –£–∫–∞–∂–∏—Ç–µ –≤–∞—à Telegram chat ID

// Middleware –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ JSON
